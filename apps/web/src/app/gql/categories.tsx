////////////////////////////////////////////////////////////////////////////////

export const GET_CATEGORIES = gql`
  query getCategories ($where: categories_bool_exp!) {
    categories (
      where: $where,
      order_by: {
        name: asc
      }
    ) {
      id
      name
      created_at
      updated_at
    }
  }
`

