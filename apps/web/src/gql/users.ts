import { gql } from "@apollo/client";

////////////////////////////////////////////////////////////////////////////////
/*----------------------------     QUERIES    --------------------------------*/
////////////////////////////////////////////////////////////////////////////////

export const GET_USER = gql`
  query getUser ($id: uuid!) {
    users_by_pk(id: $id) {
      id
      email
      first_name
      last_name
      created_at
      updated_at
    }
  }
`

